package com.bmb.app.print;


public class TtdModel {
private String kosong="";
private String ket;
private String ket2;

public TtdModel(String ket, String ket2) {
	super();
	this.ket = ket;
	this.ket2 = ket2;
}
public String getKosong() {
	return kosong;
}
public void setKosong(String kosong) {
	this.kosong = kosong;
}
public String getKet() {
	return ket;
}
public void setKet(String ket) {
	this.ket = ket;
}
public String getKet2() {
	return ket2;
}
public void setKet2(String ket2) {
	this.ket2 = ket2;
}

}
